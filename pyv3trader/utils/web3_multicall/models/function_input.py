# ------------------------------------------------------------ Imports ----------------------------------------------------------- #

# Pip
from jsoncodable import JSONCodable

# -------------------------------------------------------------------------------------------------------------------------------- #



# ----------------------------------------------------- class: FunctionInput ----------------------------------------------------- #

class FunctionInput(JSONCodable):

    # --------------------------------------------------------- Init --------------------------------------------------------- #

    def __init__(
        self,
        name: str,
        value: any,
        solidity_type: str
    ):
        self.name = name
        self.value = value
        self.solidity_type = solidity_type


